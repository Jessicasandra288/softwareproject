<div class="entry-content">
    <p>Metode Preferences Selection Index (PSI) merupakan metode untuk memecahkan multikriteria pengambilan keputusan (MCDM). 
	Dalam metode yang diusulkan itu tidak perlu untuk menetapkan kepentingan relatif antara atribut. Bahkan, tidak ada kebutuhan 
	komputasi bobot atribut yang terlibat dalam pengambilan keputusan dalam metode ini. Metode ini berguna bila ada konflik dalam 
	menentukan kepentingan relatif antar atribut. Dalam metode Preferences Selection Index (PSI), hasilnya diperoleh dengan perhitungan 
	minimal dan sederhana seperti apa adanya berdasarkan konsep statistik tanpa keharusan bobot atribut</p>
    <p>Preference Selection Index (PSI) mempunyai hasil perhitungan mudah dan alamiah, yang akan mempermudah tim pengguna dalam menentukan 
	urutan ruas jalan yang akan dilakukakan perbaikan berdasarkan kriteria yang sudah ditentukan sebelumnya.</p>
    <p>Metode Preference Selection Index (PSI) akan menghasilkan bobot tersendiri dalam tahapan penyelesaian masalahnya 
	sehingga penelitian ini lebih akurat pada setiap tahap penyelesaiannya.</p>
